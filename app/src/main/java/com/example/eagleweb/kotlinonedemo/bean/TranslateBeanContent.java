package com.example.eagleweb.kotlinonedemo.bean;

public class TranslateBeanContent {
    private String ciba_out;
    private String vendor;
    private String from;
    private String to;
    private String ciba_use;
    private int    err_no;
    private String out;

    public String getCiba_out() {
        return this.ciba_out;
    }

    public void setCiba_out(String ciba_out) {
        this.ciba_out = ciba_out;
    }

    public String getVendor() {
        return this.vendor;
    }

    public void setVendor(String vendor) {
        this.vendor = vendor;
    }

    public String getFrom() {
        return this.from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return this.to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getCiba_use() {
        return this.ciba_use;
    }

    public void setCiba_use(String ciba_use) {
        this.ciba_use = ciba_use;
    }

    public int getErr_no() {
        return this.err_no;
    }

    public void setErr_no(int err_no) {
        this.err_no = err_no;
    }

    public String getOut() {
        return this.out;
    }

    public void setOut(String out) {
        this.out = out;
    }
}
